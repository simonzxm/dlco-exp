# generate_ascii.py

# 建立 PS/2 扫描码到 ASCII 码的映射表 (十六进制)
scan_code_map = {
    0x1C: 0x61, # 'A' -> 'a'
    0x32: 0x62, # 'B' -> 'b'
    0x21: 0x63, # 'C' -> 'c'
    0x23: 0x64, # 'D' -> 'd'
    0x24: 0x65, # 'E' -> 'e'
    0x2B: 0x66, # 'F' -> 'f'
    0x34: 0x67, # 'G' -> 'g'
    0x33: 0x68, # 'H' -> 'h'
    0x43: 0x69, # 'I' -> 'i'
    0x3B: 0x6A, # 'J' -> 'j'
    0x42: 0x6B, # 'K' -> 'k'
    0x4B: 0x6C, # 'L' -> 'l'
    0x3A: 0x6D, # 'M' -> 'm'
    0x31: 0x6E, # 'N' -> 'n'
    0x44: 0x6F, # 'O' -> 'o'
    0x4D: 0x70, # 'P' -> 'p'
    0x15: 0x71, # 'Q' -> 'q'
    0x2D: 0x72, # 'R' -> 'r'
    0x1B: 0x73, # 'S' -> 's'
    0x2C: 0x74, # 'T' -> 't'
    0x3C: 0x75, # 'U' -> 'u'
    0x2A: 0x76, # 'V' -> 'v'
    0x1D: 0x77, # 'W' -> 'w'
    0x22: 0x78, # 'X' -> 'x'
    0x35: 0x79, # 'Y' -> 'y'
    0x1A: 0x7A, # 'Z' -> 'z'
    0x45: 0x30, # '0' -> '0'
    0x16: 0x31, # '1' -> '1'
    0x1E: 0x32, # '2' -> '2'
    0x26: 0x33, # '3' -> '3'
    0x25: 0x34, # '4' -> '4'
    0x2E: 0x35, # '5' -> '5'
    0x36: 0x36, # '6' -> '6'
    0x3D: 0x37, # '7' -> '7'
    0x3E: 0x38, # '8' -> '8'
    0x46: 0x39, # '9' -> '9'
}

with open("ascii.txt", "w") as f:
    for i in range(256):
        if i in scan_code_map:
            f.write(f"@{i:02X} {scan_code_map[i]:02X}\n")
        else:
            f.write(f"@{i:02X} 00\n")

print("ascii.txt 生成成功！")